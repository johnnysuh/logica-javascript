function contas(n1, n2){
    let soma = n1 + n2;
    let subtracao = n1 - n2;
    let multiplicacao = n1 * n2;
    let divisao = n1 / n2;

    console.log(`Soma é igual a: ${soma}`) 
    console.log(`Subtração é igual a: ${subtracao}`) 
    console.log(`Multiplicação é igual a: ${multiplicacao}`) 
    console.log(`Divisão é igual a: ${divisao}`) 
}

contas(10,2)